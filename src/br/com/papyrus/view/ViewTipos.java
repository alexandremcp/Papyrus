    /**
     * Método que linka o AbstractTableModel a tabela no formulário setTbTipos
     *
     * @param modelo vindo de ControllerTipos.carregar() com a lista do
     * TableModel
     */
    public void setTbTipos(AbstractTableModel modelo) {
        this.tbTipos.setModel(modelo);
        this.tbTipos.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        this.tbTipos.setSelectionBackground(Color.cyan);  //Põe cor azul na linha atual
        this.tbTipos.setSelectionForeground(Color.red);   //Põe letras em vermelho
        this.tbTipos.setBackground(Color.lightGray);      //põe cor de fundo azul
        this.tbTipos.getColumnModel().getColumn(0).setPreferredWidth(70); //Largura da coluna
        this.tbTipos.getColumnModel().getColumn(0).setResizable(true);    //Coluna redimenciosavel
        this.tbTipos.getColumnModel().getColumn(1).setPreferredWidth(600);
        this.tbTipos.getColumnModel().getColumn(1).setResizable(true);
        this.tbTipos.getColumnModel().getColumn(2).setPreferredWidth(150);
        this.tbTipos.getColumnModel().getColumn(2).setResizable(true);
    }
