    /**
     * Método que linka o AbstractTableModel a tabela no formulário
     * setTbEditoras
     *
     * @param modelo vindo de ControllerEditoras.carregar() com a lista do
     * TableModel
     */
    public void setTbEditoras(AbstractTableModel modelo) {
        this.tbEditoras.setModel(modelo);
        this.tbEditoras.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        this.tbEditoras.setSelectionBackground(Color.cyan);  //Põe cor azul na linha atual
        this.tbEditoras.setSelectionForeground(Color.red);   //Põe letras em vermelho
        this.tbEditoras.setBackground(Color.lightGray);      //põe cor de fundo azul
        this.tbEditoras.getColumnModel().getColumn(0).setPreferredWidth(70); //Largura da coluna
        this.tbEditoras.getColumnModel().getColumn(0).setResizable(true);    //Coluna redimenciosavel
        this.tbEditoras.getColumnModel().getColumn(1).setPreferredWidth(400);
        this.tbEditoras.getColumnModel().getColumn(1).setResizable(true);
        this.tbEditoras.getColumnModel().getColumn(2).setPreferredWidth(350);
        this.tbEditoras.getColumnModel().getColumn(2).setResizable(true);
    }

