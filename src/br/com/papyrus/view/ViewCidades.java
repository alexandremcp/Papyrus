    /**
     * MÃ©todo que linka o AbstractTableModel a tabela no formulÃ¡rio setTbCidades
     *
     * @param modelo vindo de ControllerCidades.carregar() com a lista do
     * TableModel
     */
    public void setTbCidades(AbstractTableModel modelo) {
        this.tbCidades.setModel(modelo);
        this.tbCidades.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        this.tbCidades.setSelectionBackground(Color.cyan);  //PÃµe cor azul na linha atual
        this.tbCidades.setSelectionForeground(Color.red);   //PÃµe letras em vermelho
        this.tbCidades.setBackground(Color.lightGray);      //pÃµe cor de fundo azul
        this.tbCidades.getColumnModel().getColumn(0).setPreferredWidth(70); //Largura da coluna
        this.tbCidades.getColumnModel().getColumn(0).setResizable(true);    //Coluna redimenciosavel
        this.tbCidades.getColumnModel().getColumn(1).setPreferredWidth(350);
        this.tbCidades.getColumnModel().getColumn(1).setResizable(true);
        this.tbCidades.getColumnModel().getColumn(2).setPreferredWidth(70);
        this.tbCidades.getColumnModel().getColumn(2).setResizable(true);
        this.tbCidades.getColumnModel().getColumn(3).setPreferredWidth(350);
        this.tbCidades.getColumnModel().getColumn(3).setResizable(true);
    }
